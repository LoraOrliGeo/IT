package module_one.lesson3_practice;

public class Task1 {
    public static void main(String[] args) {

        for (int i = -100; i <= 100; i++){
            System.out.print(i + " ");
        }

        System.out.println();
        // backwards:

        for (int i = 100; i >= -100; i--){
            System.out.print(i + " ");
        }

    }
}
